# Steps
# 1. Scraping : Extract data from HTML or API
# 2. Parsing : Convert raw data into structured format
# 3. Cleaning : Remove duplicates, handle missing values
# 4. Storing : Save data in a database or file system
# 5. Analyzing : Perform statistical analysis or machine learning
# 6. Visualizing : Create charts or graphs to represent data
# 7. Reporting : Generate reports or dashboards for insights
# 8. Monitoring : Set up alerts for data anomalies or changes

# 9. Pipeline : Automate the entire process from scraping to reporting

class LogColors:
    """
    ANSI color codes for console logging output formatting.
    
    Attributes:
        RED (str): ANSI code for red text
        YELLOW (str): ANSI code for yellow text
        RESET (str): ANSI code to reset text formatting
    """
    RED = "\033[91m"
    YELLOW = "\033[93m"
    RESET = "\033[0m"
